public class Decimals {
    public static void main(String[] args) {
        double percentage = 73.5;
        System.out.println(percentage + " of percentage are made up of chocolate");

        double wallet = 20; // always use double for math calculations. This will be stored as a double: 20.0000
        int people = 3;
        System.out.println(wallet / people);
    }
}